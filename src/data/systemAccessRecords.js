export function getSystemAccessRecords(caseId) {
  return systemAccessRecordsByCase[caseId] ?? [];
}

export function getSystemAccessRecordsByTool(caseId, toolName) {
  const records = getSystemAccessRecords(caseId);
  if (!toolName || toolName === 'System Access Lane') return records;
  return records.filter((record) => record.tool === toolName);
}
