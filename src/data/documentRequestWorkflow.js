import { buildCustomerDocumentResponse, getCustomerDocumentResponseOutcome } from './customerDocumentResponses.js';
import { getCaseDocumentRequests, getCaseDocuments } from './documentRecords.js';

const receivedStatuses = new Set(['Received', 'Incomplete', 'Received Late', 'Approved', 'Pending Review']);

function normalizedDocumentStatus(status = '') {
  if (status === 'Available') return 'Pending Review';
  if (status === 'Pending') return 'Not Requested';
  return status || 'Not Requested';
}

function requirementFor(document = {}) {
  return /optional/i.test(`${document.folder ?? ''} ${document.type ?? ''}`) ? 'Optional' : 'Required';
}

function fieldsText(fields = []) {
  return fields.map(([label, value]) => `${label}: ${value}`).join(' | ');
}

function attemptsFor(requestUpdates = {}, documentId = '') {
  const saved = requestUpdates[documentId];
  return Array.isArray(saved?.attempts) ? saved.attempts : [];
}

export function getPaperworkRequestTemplates(activeCase = {}) {
  return getCaseDocumentRequests(activeCase).filter((document) => !document.pages?.length);
}

export function createPaperworkAttempt({
  activeCase = {},
  document = {},
  reason,
  dueDate,
  requestedDate,
  deliveryChannel,
  attemptId,
}) {
  const resolvedAttemptId = attemptId ?? `ATT-${Date.now()}`;
  return {
    attemptId: resolvedAttemptId,
    requestId: `${document.id}-REQ-${resolvedAttemptId.replace(/^ATT-/, '')}`,
    responseId: '',
    sourceDocumentId: document.id,
    documentTitle: document.title,
    category: document.folder,
    reason,
    dueDate,
    requestedDate,
    recipient: activeCase.person ?? 'Customer on the active case',
    requestDeliveryChannel: deliveryChannel,
    responseOutcome: getCustomerDocumentResponseOutcome(activeCase, document),
    responseStatus: '',
    responseCheckedAt: '',
    responseChannel: '',
    receivedDate: 'Not received',
    customerSubmission: null,
    unread: false,
    reviewerNotes: `Paperwork request sent through ${deliveryChannel}. Review any returned source document before documenting an outcome.`,
  };
}

export function applyCustomerResponse({ activeCase = {}, document = {}, attempt = {}, checkedAt }) {
  const response = buildCustomerDocumentResponse({
    activeCase,
    document,
    outcome: attempt.responseOutcome,
    receivedDate: checkedAt,
  });
  const responseId = response.customerSubmission?.pages?.length
    ? `${document.id}-CUS-${String(attempt.attemptId).replace(/^ATT-/, '')}`
    : '';
  return {
    ...attempt,
    responseId,
    responseStatus: response.status,
    responseCheckedAt: response.responseCheckedAt,
    responseChannel: response.responseChannel,
    receivedDate: response.receivedDate,
    customerSubmission: response.customerSubmission,
    unread: Boolean(response.customerSubmission?.pages?.length),
    responseReason: response.reason,
    reviewerNotes: response.reviewerNotes,
  };
}

function receivedCaseRecord(activeCase, document) {
  const status = normalizedDocumentStatus(document.requestStatus ?? document.status);
  return {
    id: document.id,
    sourceDocumentId: document.id,
    documentViewerId: document.id,
    attemptId: '',
    recordKind: 'customer-submission',
    documentType: document.title,
    category: document.folder,
    status,
    reason: 'Customer-submitted paperwork is available for source-document review and comparison.',
    requirement: requirementFor(document),
    dueDate: 'Not applicable',
    authenticity: document.authenticity,
    reviewerNotes: document.summary,
    linkedCase: activeCase.id,
    linkedTool: 'Document Viewer',
    receivedDate: receivedStatuses.has(status) ? document.received : 'Not received',
    requestedDate: 'No agent request required',
    recipient: activeCase.person ?? 'Customer on the active case',
    sender: document.source ?? activeCase.person ?? 'Customer on the active case',
    requestDeliveryChannel: 'No agent request required',
    deliveryChannel: 'Customer-provided case intake',
    pagesAvailable: Boolean(document.pages?.length),
    fields: fieldsText(document.fields),
    responseOutcome: '',
    responseCheckedAt: '',
    unread: false,
  };
}

function outboundRecord(activeCase, document, attempt) {
  const noResponse = attempt.responseStatus === 'No Response';
  return {
    id: attempt.requestId,
    sourceDocumentId: document.id,
    documentViewerId: '',
    attemptId: attempt.attemptId,
    recordKind: 'outbound-request',
    documentType: `${document.title} request`,
    requestedDocumentType: document.title,
    category: document.folder,
    status: noResponse ? 'No Response' : 'Requested',
    reason: noResponse ? attempt.responseReason : attempt.reason,
    requirement: requirementFor(document),
    dueDate: attempt.dueDate,
    authenticity: 'Outbound request record generated by the agent action.',
    reviewerNotes: attempt.reviewerNotes,
    linkedCase: activeCase.id,
    linkedTool: 'Document Viewer',
    receivedDate: 'Not received',
    requestedDate: attempt.requestedDate,
    recipient: attempt.recipient,
    sender: 'Fraud Academy Document Services',
    requestDeliveryChannel: attempt.requestDeliveryChannel,
    deliveryChannel: attempt.requestDeliveryChannel,
    pagesAvailable: false,
    fields: `Requested document: ${document.title} | Delivery: ${attempt.requestDeliveryChannel} | Due: ${attempt.dueDate}`,
    responseOutcome: attempt.responseOutcome,
    responseCheckedAt: attempt.responseCheckedAt,
    unread: false,
  };
}

function inboundResponseRecord(activeCase, document, attempt) {
  const submission = attempt.customerSubmission;
  return {
    id: attempt.responseId,
    sourceDocumentId: document.id,
    documentViewerId: attempt.responseId,
    attemptId: attempt.attemptId,
    recordKind: 'customer-submission',
    documentType: document.title,
    category: document.folder,
    status: attempt.responseStatus,
    reason: attempt.responseReason,
    requirement: requirementFor(document),
    dueDate: attempt.dueDate,
    authenticity: submission.authenticity,
    reviewerNotes: attempt.reviewerNotes,
    linkedCase: activeCase.id,
    linkedTool: 'Document Viewer',
    receivedDate: attempt.receivedDate,
    requestedDate: attempt.requestedDate,
    recipient: 'Fraud Academy Chargebacks',
    sender: activeCase.person ?? 'Training customer',
    requestDeliveryChannel: attempt.requestDeliveryChannel,
    deliveryChannel: attempt.responseChannel,
    pagesAvailable: true,
    fields: fieldsText(submission.fields),
    responseOutcome: attempt.responseOutcome,
    responseCheckedAt: attempt.responseCheckedAt,
    unread: Boolean(attempt.unread),
  };
}

export function buildPaperworkInboxRecords(activeCase = {}, requestUpdates = {}) {
  const sourceDocuments = getCaseDocumentRequests(activeCase);
  const receivedDocuments = sourceDocuments
    .filter((document) => document.pages?.length)
    .map((document) => receivedCaseRecord(activeCase, document));
  const conversationRecords = sourceDocuments.flatMap((document) => attemptsFor(requestUpdates, document.id).flatMap((attempt) => {
    const records = [outboundRecord(activeCase, document, attempt)];
    if (attempt.customerSubmission?.pages?.length && attempt.responseId) {
      records.push(inboundResponseRecord(activeCase, document, attempt));
    }
    return records;
  }));
  return [...receivedDocuments, ...conversationRecords];
}

export function buildCustomerResponseDocuments(activeCase = {}, requestUpdates = {}) {
  const sourceDocuments = getCaseDocuments(activeCase);
  return sourceDocuments.flatMap((document) => attemptsFor(requestUpdates, document.id)
    .filter((attempt) => attempt.customerSubmission?.pages?.length && attempt.responseId)
    .map((attempt) => ({
      ...document,
      id: attempt.responseId,
      title: document.title,
      folder: 'Customer Evidence',
      status: attempt.responseStatus,
      reviewStatus: attempt.responseStatus === 'Incomplete'
        ? 'Incomplete — investigator review required'
        : attempt.responseStatus === 'Received Late'
          ? 'Pending Review — received late'
          : 'Pending Review',
      requestStatus: attempt.responseStatus,
      pages: attempt.customerSubmission.pages,
      fields: attempt.customerSubmission.fields ?? document.fields,
      source: attempt.customerSubmission.source ?? document.source,
      reference: attempt.customerSubmission.reference ?? attempt.responseId,
      summary: attempt.customerSubmission.summary ?? document.summary,
      authenticity: attempt.customerSubmission.authenticity ?? document.authenticity,
      extractionConfidence: attempt.customerSubmission.extractionConfidence ?? document.extractionConfidence,
      received: attempt.receivedDate,
      requestDueDate: attempt.dueDate,
      requestDeliveryChannel: attempt.requestDeliveryChannel,
      responseChannel: attempt.responseChannel,
      investigatorNote: 'Review the customer-supplied page itself. Record what is visible, what is missing, and how its dates compare with the claim and merchant records.',
    })));
}
